import { PrismaClient, Role } from '@prisma/client';

const prisma = new PrismaClient();

const users = [
  {
    email: 'admin@example.com',
    name: 'Admin User',
    password: '123456', // ‚ö†Ô∏è Replace with hashed passwords in real apps
    role: Role.admin,
  },
  {
    email: 'user@example.com',
    name: 'Regular User',
    password: '123456',
    role: Role.user,
  },
];

async function seed() {
  console.log('üöÄ Starting seed...');

  // Step 1: Clean up old data (in correct order if relations exist)
  // await prisma.$transaction([prisma.user.deleteMany()]);
  await prisma.user.deleteMany();

  // Step 2: Seed fresh data
  await prisma.user.createMany({
    data: users,
  });

  console.log('‚úÖ Seed complete');
}

// eslint-disable-next-line
(async () => {
  try {
    await seed();
  } catch (e) {
    console.error('‚ùå Seeding failed:', e);
    process.exit(1);
  } finally {
    try {
      await prisma.$disconnect();
    } catch (disconnectError) {
      console.error('‚ö†Ô∏è Failed to disconnect Prisma:', disconnectError);
    }
  }
})();
